package com.example.fellowtraveller;

public class RateUserContainerItem {
    private UserB user;
    private int friendly;
    private int reliable;
    private int careful;
    private int consistent;

    public UserB getUser() {
        return user;
    }

    public void setUser(UserB user) {
        this.user = user;
    }

    public int getFriendly() {
        return friendly;
    }

    public void setFriendly(int friendly) {
        this.friendly = friendly;
    }

    public int getReliable() {
        return reliable;
    }

    public void setReliable(int reliable) {
        this.reliable = reliable;
    }

    public int getCareful() {
        return careful;
    }

    public void setCareful(int careful) {
        this.careful = careful;
    }

    public int getConsistent() {
        return consistent;
    }

    public void setConsistent(int consistent) {
        this.consistent = consistent;
    }
}
